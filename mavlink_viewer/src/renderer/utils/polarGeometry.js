export const antarcticaCoastline = []
// 生成南极洲的近似圆形边界
for (let lon = -180; lon <= 180; lon += 5) {
  // 使用正弦波模拟南极洲的不规则海岸线
  const lat = -68 - 
              Math.sin(lon * Math.PI / 60) * 3 -
              Math.sin(lon * Math.PI / 30) * 2 -
              Math.cos(lon * Math.PI / 45) * 2.5
  antarcticaCoastline.push([lon, Math.max(lat, -85)])
}

